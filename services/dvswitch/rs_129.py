# Generator polynomial coefficients (only first 3 actually used since NPAR=3).
POLY = (64, 56, 14, 1)
NPAR = 3

# DMR parity XOR masks per data type (ETSI clause B.3.6, Table B.10).
VOICE_LC_HEADER_CRC_MASK    = (0x96, 0x96, 0x96)
TERMINATOR_WITH_LC_CRC_MASK = (0x99, 0x99, 0x99)


def gmult(a: int, b: int) -> int:
    """GF(2^8) multiplication using log/antilog tables."""
    if a == 0 or b == 0:
        return 0
    return EXP_TABLE[LOG_TABLE[a] + LOG_TABLE[b]]


def rs_encode_9(msg: bytes) -> tuple[int, int, int]:
    """Encode 9 bytes of LC content → 3 RS(12,9) parity bytes.

    Returns (parity_0, parity_1, parity_2) per the MMDVMHost LFSR
    convention. The bytes that get appended to the LC trailer are:
        lcData[9]  = parity[2] XOR mask[0]
        lcData[10] = parity[1] XOR mask[1]
        lcData[11] = parity[0] XOR mask[2]
    (note the REVERSED parity-byte order in the trailer).
    """
    if len(msg) != 9:
        raise ValueError(f"RS(12,9) message must be 9 bytes, got {len(msg)}")
    parity = [0, 0, 0, 0]   # NPAR+1 = 4 elements
    for i in range(9):
        dbyte = msg[i] ^ parity[NPAR - 1]
        for j in range(NPAR - 1, 0, -1):
            parity[j] = parity[j - 1] ^ gmult(POLY[j], dbyte)
        parity[0] = gmult(POLY[0], dbyte)
    return parity[0], parity[1], parity[2]


def build_lc_trailer(
    lc_9bytes: bytes,
    data_type_mask: tuple[int, int, int] = VOICE_LC_HEADER_CRC_MASK,
) -> tuple[int, int, int]:
    """Compute the 3 trailer bytes (parity XOR mask) for a 9-byte LC.

    Returns (lc_byte_9, lc_byte_10, lc_byte_11) ready to append to
    the LC, forming the 12-byte input for BPTC(196,96).
    """
    p0, p1, p2 = rs_encode_9(lc_9bytes)
    return (
        p2 ^ data_type_mask[0],
        p1 ^ data_type_mask[1],
        p0 ^ data_type_mask[2],
    )


def _selftest() -> int:
    # gmult sanity
    assert gmult(0, 5) == 0
    assert gmult(5, 0) == 0
    assert gmult(1, 1) == 1
    print("[PASS] gmult zero-cases + identity")

    # Encode all-zero LC — should produce all-zero parity.
    p = rs_encode_9(b"\x00" * 9)
    assert p == (0, 0, 0), f"all-zero LC parity = {p}, expected (0,0,0)"
    print("[PASS] all-zero LC → zero parity")

    # The classic test vector: encode a non-trivial LC and verify
    # that the trailing CRC bytes (after mask) round-trip through
    # MMDVMHost's check() function. We don't have check() ported,
    # so use a known-good case:
    #
    # For LC = group voice, FLCO=0, FID=0, SO=0, dst=3127 (0x000C37),
    # src=3104410 (0x2F5D5A), MMDVMHost will produce specific RS
    # parity. We can validate by re-encoding and checking the parity
    # bytes are deterministic + non-zero.
    lc = bytes.fromhex("000000000c372f5d5a")
    p = rs_encode_9(lc)
    print(f"[INFO] LC=src 3104410 → TG 3127 RS parity = {p[0]:#04x} {p[1]:#04x} {p[2]:#04x}")

    trailer = build_lc_trailer(lc, VOICE_LC_HEADER_CRC_MASK)
    print(f"[INFO] LC header trailer (masked) = {trailer[0]:#04x} {trailer[1]:#04x} {trailer[2]:#04x}")
    term_trailer = build_lc_trailer(lc, TERMINATOR_WITH_LC_CRC_MASK)
    print(f"[INFO] Terminator trailer (masked) = {term_trailer[0]:#04x} {term_trailer[1]:#04x} {term_trailer[2]:#04x}")

    # Sanity: header vs terminator trailers should differ exactly by
    # 0x96 XOR 0x99 = 0x0F in each byte position.
    delta = [a ^ b for a, b in zip(trailer, term_trailer)]
    assert delta == [0x0F, 0x0F, 0x0F], \
        f"header→terminator delta = {delta}, expected [0x0f]*3"
    print("[PASS] header→terminator trailer differs only by mask XOR (0x0F per byte)")

    print("\nOK — rs_129 module validates structurally.")
    return 0


if __name__ == "__main__":
    raise SystemExit(_selftest())
